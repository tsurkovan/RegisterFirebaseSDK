import Foundation           // → Базовые типы: String, Bool, Error, Date и др.
import FirebaseAuth         // → Firebase-авторизация: signIn, signUp, Auth.auth()

// ─────────────────────────────────────────────────────────────
// ОБЪЯВЛЕНИЕ КЛАССА
// ─────────────────────────────────────────────────────────────

@Observable                 // → 🔔 Автоматически отслеживает изменения ВСЕХ var-свойств
                            // → 🔄 При изменении любого свойства → уведомляет подписчиков (View)
                            // → ✅ Замена @Published + ObservableObject (iOS 17+)

@MainActor                  // → 🧵 Гарантирует: все методы/свойства выполняются на ГЛАВНОМ потоке
                            // → 🎯 Критично для обновления UI: SwiftUI требует main-поток
                            // → ⚡ Избавляет от необходимости писать await MainActor.run { ... }

class RegisterViewModel {   // → 📦 ViewModel в паттерне MVVM: связывает View и бизнес-логику
    
    // ═════════════════════════════════════════════════════════
    // MARK: - Свойства состояния (⚡ Реактивные данные для View)
    // ═════════════════════════════════════════════════════════
    
    var email = ""          // → 📧 Ввод пользователя: привязывается к TextField через $viewModel.email
                            // → 🔗 Binding позволяет: читать + писать + авто-обновление UI
    
    var password = ""       // → 🔐 Ввод пароля: привязывается к SecureField
                            // → 🔒 SecureField маскирует ввод, но данные хранятся как String
    
    var isLoading = false   // → ⏳ Флаг загрузки: управляет отображением ProgressView
                            // → 🎯 true = показать индикатор, false = скрыть
    
    var errorMessage: String?  // → ⚠️ Опциональная ошибка: nil = нет ошибки, String = текст для показа
                               // → ❓ Тип String? позволяет условно рендерить: if let error = ...
    
    var isLoggedIn = false  // → ✅ Флаг успешной авторизации: управляет навигацией
                            // → 🔄 При смене на true → View может перейти на главный экран
    
    var isSignUpMode = false // → 🎛️ Переключатель режима: false = вход, true = регистрация
                             // → 🔄 Влияет на заголовок, текст кнопки и вызываемый метод
    
    // ═════════════════════════════════════════════════════════
    // MARK: - Зависимости (🔗 Внешние сервисы)
    // ═════════════════════════════════════════════════════════
    
    private let registerService = RegisterService.shared
                            // → 🔐 private: инкапсуляция — View не должен знать о сервисе
                            // → 🧱 let: зависимость неизменна в течение жизни ViewModel
                            // → 🌐 .shared: Singleton — единый источник авторизации для всего приложения
                            // → 📡 RegisterService: абстракция над FirebaseAuth (упрощает тестирование)
    
    // ═════════════════════════════════════════════════════════
    // MARK: - Вычисляемые свойства (🧮 Логика без хранения)
    // ═════════════════════════════════════════════════════════
    
    var isFormValid: Bool {              // → ✅ Не хранит значение, вычисляет «на лету» при обращении
        email.contains("@") &&           // → 📧 Проверка 1: email содержит символ @ (базовая валидация)
        password.count >= 6              // → 🔐 Проверка 2: пароль минимум 6 символов (требование Firebase)
    }                                    // → 🔄 Авто-пересчёт: при изменении email/password → новое значение
                                         // → 🎯 Использование: .disabled(!viewModel.isFormValid) в кнопке
    
    var title: String {                  // → 🏷️ Динамический заголовок экрана
        isSignUpMode ? "Регистрация" : "Вход"
    }                                    // → 🎯 Тернарный оператор: если режим регистрации → "Регистрация", иначе → "Вход"
                                         // → 🔄 Авто-обновление: при toggleAuthMode() → заголовок меняется сам
    
    var buttonTitle: String {            // → 🔘 Динамический текст кнопки
        isSignUpMode ? "Зарегистрироваться" : "Войти"
    }                                    // → 🎯 Единая точка правды: не дублировать текст в View
    
    // ═════════════════════════════════════════════════════════
    // MARK: - Действия: Вход (🔐 async метод)
    // ═════════════════════════════════════════════════════════
    
    func signIn() async {                // → ⚡ async: метод может приостанавливаться (ждать сеть/БД)
                                         // → 🎯 Вызов: Task { await viewModel.signIn() } из View
        
        
        // ── ШАГ 1: Валидация (ранний выход) ───────────────────
        guard isFormValid else {                     // → 🛡️ guard: если условие НЕ выполнено → выполняем else и выходим
            errorMessage = "Заполните все поля корректно"  // → ⚠️ Показываем пользователю причину блокировки
            return                                     // → 🚫 Прерываем выполнение: не запускаем сетевой запрос
        }                                            // → ✅ Если валидация пройдена → код продолжается ниже
        
        // ── ШАГ 2: Подготовка состояния перед запросом ───────
        isLoading = true               // → ⏳ Показываем индикатор загрузки (UI реагирует мгновенно)
        errorMessage = nil             // → 🧹 Очищаем предыдущую ошибку (чтобы не показывать старое)
        
        
        
        // ── ШАГ 3: Попытка выполнения асинхронной операции ───
        
        
        
        
        do {                                             // → 🔄 Блок try-catch: ловим ошибки асинхронного вызова
            try await registerService.signIn(               // → 📡 try: метод может выбросить ошибку
                email: email,                               // → 📧 Передаём email из свойства ViewModel
                password: password                          // → 🔐 Передаём пароль из свойства ViewModel
            )                                    // → ⏳ await: ждём завершения сетевого запроса (не блокируя UI)
            
            isLoggedIn = true                    // → ✅ Успех: меняем флаг → View может перейти на главный экран
        } catch {                                         // → ❌ Ошибка: код внутри do выбросил исключение
            // ✅ Теперь используем общий AuthError
            
            errorMessage = AuthError.fromFirebase(error)    // → 🎭 Адаптер: преобразуем техническую ошибку Firebase
                .errorDescription                           // → 📝 в человекочитаемый текст на русском
        }                                                   // → 🎯 Пользователь видит понятное сообщение, а не "AuthError 17001"
        
        // ── ШАГ 4: Завершение загрузки (всегда!) ─────────────
        isLoading = false              // → 🏁 Скрываем индикатор: выполняется и при успехе, и при ошибке
    }                                  // → 🎯 Важно: не забыть сбросить isLoading, иначе кнопка «зависнет»
    
    // ═════════════════════════════════════════════════════════
    // MARK: - Действия: Регистрация (📝 async метод)
    // ═════════════════════════════════════════════════════════
    
    func signUp() async {              // → ⚡ Аналогично signIn(), но для создания нового аккаунта
                                       // → 🎯 Почему отдельный метод? Разная логика, легче тестировать
        
        // ── ШАГ 1: Валидация ─────────────────────────────────
        guard isFormValid else {                   // → 🛡️ Та же валидация: форма должна быть корректной
            errorMessage = "Заполните все поля корректно"  // → ⚠️ То же сообщение об ошибке
            return                                   // → 🚫 Ранний выход при невалидных данных
        }
        
        // ── ШАГ 2: Подготовка состояния ──────────────────────
        isLoading = true               // → ⏳ Показываем загрузку
        errorMessage = nil             // → 🧹 Очищаем старую ошибку
        
        // ── ШАГ 3: Асинхронный вызов регистрации ─────────────
        do {
            try await registerService.signUp(               // → 📡 Вызываем метод создания аккаунта
                email: email,                               // → 📧 Email нового пользователя
                password: password                          // → 🔐 Пароль (Firebase проверит сложность)
            )                                               // → ⏳ Ждём ответа от сервера Firebase
            isLoggedIn = true                               // → ✅ Успех: пользователь авторизован → переходим в приложение
        } catch {                                           // → ❌ Обработка ошибки
            // ✅ Теперь используем общий AuthError
            
            errorMessage = AuthError.fromFirebase(error)    // → 🎭 Преобразуем ошибку Firebase
                .errorDescription                           // → 📝 в понятный текст
        }
        
        // ── ШАГ 4: Завершение ────────────────────────────────
        isLoading = false              // → 🏁 Всегда сбрасываем индикатор загрузки
    }
    
    // ═════════════════════════════════════════════════════════
    // MARK: - Действия: Выход (🚪 синхронный метод)
    // ═════════════════════════════════════════════════════════
    
    func signOut() {                     // → 🚪 Обычный (не async) метод: signOut() в Firebase быстрый
                                         // → 🎯 Вызов: viewModel.signOut() из кнопки «Выйти»
        
        try? registerService.signOut()   // → 🔐 try?: безопасный вызов — если ошибка, игнорируем
                                         // → 🤷‍♂️ Почему?: если пользователь уже вышел, нет смысла крашить приложение
                                         // → 🎯 Альтернатива: do { try ... } catch { log(error) } для отладки
        
        // ── Сброс состояния формы ────────────────────────────
        
        isLoggedIn = false               // → ❌ Сбрасываем флаг: возвращаем на экран авторизации
        email = ""                       // → 🧹 Очищаем email: конфиденциальность + чистый старт
        password = ""                    // → 🧹 Очищаем пароль: безопасность
        errorMessage = nil               // → 🧹 Очищаем ошибку: чтобы не показывать старое при следующем входе
    }                                    // → 🎯 Все сбросы в одном месте: легче поддерживать и не забыть поле
    
    // ═════════════════════════════════════════════════════════
    // MARK: - Действия: Переключение режима (🎛️ синхронный)
    // ═════════════════════════════════════════════════════════
    
    func toggleAuthMode() {              // → 🎛️ Переключает между «Вход» и «Регистрация»
                                         // → 🎯 Вызов: Button { viewModel.toggleAuthMode() }
        
        isSignUpMode.toggle()            // → 🔄 Встроенный метод Bool: false → true, true → false
                                         // → 🎯 Короче и читаемее, чем: isSignUpMode = !isSignUpMode
        
        // ── Сброс полей при смене режима ─────────────────────
        email = ""                       // → 🧹 Очищаем email: новый режим = начинаем с чистого листа
        password = ""                    // → 🧹 Очищаем пароль: чтобы не подставлять старое в другую форму
        errorMessage = nil               // → 🧹 Очищаем ошибку: ошибка от входа не актуальна для регистрации
    }                                    // → 🔄 Благодаря @Observable: изменение isSignUpMode → авто-обновление title/buttonTitle в View
}
