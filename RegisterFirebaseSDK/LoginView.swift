import SwiftUI  // 📦 Импорт фреймворка SwiftUI для создания пользовательского интерфейса

struct LoginView: View {  // 🏗️ Объявление структуры LoginView, соответствующей протоколу View
    
    @State private var viewModel = RegisterViewModel()  // 🔄 Создание экземпляра ViewModel с отслеживанием изменений (для @Observable используем @State)
    @FocusState private var isEmailFocused: Bool  // 🎯 Отслеживание фокуса на поле ввода email (для управления клавиатурой)
    
    // 👁️ ДОБАВЛЕНО: Состояние видимости пароля (переключатель "глаз")
    @State private var isPasswordVisible = false  // 🔑 По умолчанию: false = пароль скрыт (••••••)
    
    var body: some View {  // 🎨 Основной body View — описывает иерархию пользовательского интерфейса
        NavigationView {  // 🧭 Контейнер навигации для переходов между экранами
            ZStack {  // 📚 Контейнер для наложения элементов друг на друга (фон + контент)
                
                // 🌈 ============================================
                // 🎨 ГРАДИЕНТНЫЙ ФОН
                // 🌈 ============================================
                LinearGradient(  // 🎨 Создание линейного градиента
                    gradient: Gradient(colors: [  // 🎭 Определение градиента с массивом цветов
                        Color.blue.opacity(0.1),  // 💙 Полупрозрачный синий цвет (прозрачность 10%)
                        Color.purple.opacity(0.1)  // 💜 Полупрозрачный фиолетовый цвет (прозрачность 10%)
                    ]),
                    startPoint: .topLeading,  // 📍 Начальная точка градиента (верхний левый угол)
                    endPoint: .bottomTrailing  // 📍 Конечная точка градиента (нижний правый угол)
                )
                .ignoresSafeArea()  // 📐 Растягивание фона на всю область экрана (игнорируя safe area)
                
                // 📜 ============================================
                // 📋 ПРОКРУЧИВАЕМЫЙ КОНТЕНТ
                // 📜 ============================================
                ScrollView {  // 📄 Контейнер с вертикальной прокруткой для адаптивности
                    VStack(spacing: 30) {  // ⬇️ Вертикальный стек с отступом 30 пунктов между элементами
                        
                        // 🔐 ============================================
                        // 🖼️ ЛОГОТИП
                        // 🔐 ============================================
                        Image(systemName: "lock.shield.fill")  // 🔒 SF Symbol: щит с замком (иконка безопасности)
                            .font(.system(size: 80))  // 🔤 Установка размера шрифта/иконки: 80 пунктов
                            .foregroundColor(.blue)  // 💙 Установка цвета иконки: синий
                            .padding(.top, 40)  // 📏 Добавление верхнего отступа: 40 пунктов
                        
                        // 📝 ============================================
                        // 🏷️ ЗАГОЛОВОК ЭКРАНА
                        // 📝 ============================================
                        Text(viewModel.isSignUpMode ? "Создать аккаунт" : "Вход")  // 📄 Динамический текст: если режим регистрации — "Создать аккаунт", иначе "Вход"
                            .font(.largeTitle)  // 🔤 Установка размера шрифта: крупный заголовок
                            .fontWeight(.bold)  // 💪 Установка насыщенности шрифта: жирный
                        
                        // 📥 ============================================
                        // 📧 ПОЛЯ ВВОДА ДАННЫХ
                        // 📥 ============================================
                        VStack(spacing: 16) {  // ⬇️ Вертикальный стек для полей ввода с отступом 16 пунктов
                            
                            CustomTextField(  // 📝 Кастомное текстовое поле для email
                                placeholder: "Email",  // 💭 Текст-подсказка: "Email"
                                text: $viewModel.email  // 🔗 Двусторонняя привязка к свойству email в ViewModel
                            )
                            .focused($isEmailFocused)  // 🎯 Привязка состояния фокуса к переменной isEmailFocused
                            .keyboardType(.emailAddress)  // ⌨️ Установка типа клавиатуры: для email (с @ и .)
                            .autocapitalization(.none)  // 🔤 Отключение авто-капитализации (все буквы строчные)
                            
                            // 🔐 ============================================
                            // 👁️ ПОЛЕ ПАРОЛЯ С КНОПКОЙ ВИДИМОСТИ
                            // 🔐 ============================================
                            HStack {  // 📦 Горизонтальный контейнер: поле пароля + кнопка 👁️ в одну строку
                                                            
                                // 📝 Основное поле ввода пароля
                                CustomTextField(  // 🔒 Кастомное текстовое поле для пароля
                                    placeholder: "Пароль",  // 💭 Текст-подсказка: "Пароль"
                                    text: $viewModel.password,  // 🔗 Двусторонняя привязка к свойству password в ViewModel
                                    isSecure: !isPasswordVisible  // 🔐🔓 ИНВЕРСИЯ:
                                    // • isPasswordVisible = false → isSecure = true (••••• скрыто)
                                    // • isPasswordVisible = true → isSecure = false (12345 видно)
                                )
                                                            
                                // 👁️ Кнопка-переключатель видимости пароля
                                Button(action: {
                                    // 🔄 toggle() меняет значение на противоположное: false ↔ true
                                    // При нажатии: пароль то скрывается, то показывается
                                    
                                    // 📝 ЛОГИРОВАНИЕ: отслеживаем переключение видимости пароля
                                    print("👁️ [PasswordVisibility] Переключаем: было \(isPasswordVisible) → станет \(!isPasswordVisible)")
                                    
                                    isPasswordVisible.toggle()
                                    
                                    // 📝 ЛОГИРОВАНИЕ: подтверждаем изменение
                                    print("👁️ [PasswordVisibility] Теперь: isPasswordVisible = \(isPasswordVisible)")
                                    
                                }) {
                                    // 🖼️ Иконка из SF Symbols (системные иконки Apple)
                                    Image(systemName: isPasswordVisible ? "eye.slash.fill" : "eye.fill")
                                        // 🎨 Логика выбора иконки (тернарный оператор ?:):
                                        // • isPasswordVisible = true (пароль ВИДЕН) → "eye.slash.fill" 👁️‍🗨️
                                        //   Значит: нажми чтобы СКРЫТЬ
                                        // • isPasswordVisible = false (пароль СКРЫТ) → "eye.fill" 👁️
                                        //   Значит: нажми чтобы ПОКАЗАТЬ
                                        .foregroundColor(.secondary)  // 🎨 Цвет: secondary (серый, неакцентный)
                                        .imageScale(.medium)  // 📏 Размер иконки: medium (можно .small/.large)
                                }
                                // ✅ Результат: поле и кнопка 👁️ на одной строке
                            }
                            // 💡 Почему инверсия (!)?
                            // isSecure=true → СКРЫТЬ символы (•••)
                            // isPasswordVisible=true → ПОКАЗАТЬ символы (123)
                            // Это противоположности → нужна инверсия !
                                                        
                        }
                        .padding(.horizontal, 40)  // 📏 Горизонтальные отступы: 40 пунктов слева и справа
                        
                        // ⚠️ ============================================
                        // 🚨 СООБЩЕНИЕ ОБ ОШИБКЕ
                        // ⚠️ ============================================
                        if let error = viewModel.errorMessage {  // ❓ Условное отображение: если есть ошибка (errorMessage не nil)
                            HStack {  // ➡️ Горизонтальный стек для иконки и текста ошибки
                                Image(systemName: "exclamationmark.triangle.fill")  // ⚠️ SF Symbol: треугольник с восклицательным знаком
                                Text(error)  // 📄 Отображение текста ошибки из ViewModel
                            }
                            .font(.caption)  // 🔤 Размер шрифта: подпись (мелкий текст)
                            .foregroundColor(.red)  // 🔴 Цвет текста: красный
                            .padding(12)  // 📏 Внутренний отступ: 12 пунктов со всех сторон
                            .background(Color.red.opacity(0.1))  // 🎨 Фон: светло-красный (прозрачность 10%)
                            .cornerRadius(8)  //  Скругление углов: 8 пунктов
                            .padding(.horizontal, 40)  // 📏 Горизонтальные отступы: 40 пунктов
                            .transition(.move(edge: .top).combined(with: .opacity))  // 🎬 Анимация появления: сдвиг сверху + плавное проявление
                        }
                        
                        // 🔘 ============================================
                        // 🎯 КНОПКА ДЕЙСТВИЯ
                        // 🔘 ============================================
                        CustomButton(  // 🔵 Кастомная кнопка для входа/регистрации
                            title: viewModel.isSignUpMode ? "Зарегистрироваться" : "Войти",  // 📄 Динамический текст кнопки в зависимости от режима
                            action: {  // ⚡ Действие при нажатии на кнопку
                                
                                // 📝 ЛОГИРОВАНИЕ: начало процесса входа/регистрации
                                print("\n🔥 ============================================")
                                print("🔥 [LoginView] НАЧАЛО: нажата кнопка '\(viewModel.isSignUpMode ? "Зарегистрироваться" : "Войти")'")
                                print("🔥 ============================================")
                                                                
                                // 📝 ЛОГИРОВАНИЕ: выводим текущие данные формы
                                print("📧 [LoginView] Email: '\(viewModel.email)'")
                                print("🔐 [LoginView] Password: '\(viewModel.password)'")
                                print("📝 [LoginView] Режим: \(viewModel.isSignUpMode ? "РЕГИСТРАЦИЯ" : "ВХОД")")
                                print("✅ [LoginView] Форма валидна: \(viewModel.isFormValid)")
                                print("⏳ [LoginView] Загрузка: \(viewModel.isLoading)")
                                
                                Task {  // 🔄 Создание асинхронной задачи (для вызова async функций)
                                    
                                    // 📝 ЛОГИРОВАНИЕ: вход в асинхронную задачу
                                    print("🧵 [LoginView] Task: создана асинхронная задача")
                                    
                                    if viewModel.isSignUpMode {  // ❓ Проверка режима: если регистрация
                                        
                                        // 📝 ЛОГИРОВАНИЕ: вызов регистрации
                                        print("📝 [LoginView] Вызов: await viewModel.signUp()")
                                        
                                        await viewModel.signUp()  // 📝 Вызов асинхронного метода регистрации (ожидание завершения)
                                        
                                        // 📝 ЛОГИРОВАНИЕ
                                        print("✅ [LoginView] signUp() завершен")
                                        
                                    } else {  // ➡️ Иначе (режим входа)
                                        
                                        // 📝 ЛОГИРОВАНИЕ: вызов входа
                                        print("🔑 [LoginView] Вызов: await viewModel.signIn()")
                                        
                                        await viewModel.signIn()  // 🔑 Вызов асинхронного метода входа (ожидание завершения)
                                        
                                        // 📝 ЛОГИРОВАНИЕ
                                        print("✅ [LoginView] signIn() завершен")
                                        
                                    }
                                    
                                    // 📝 ЛОГИРОВАНИЕ: после выполнения операции
                                    print("🏁 [LoginView] Операция завершена")
                                    print("🔥 ============================================\n")
                                    
                                }
                            },
                            isLoading: viewModel.isLoading,  // ⏳ Флаг загрузки: показывает индикатор при выполнении операции
                            isEnabled: viewModel.isFormValid  // ✅ Флаг доступности: кнопка активна только если форма валидна
                        )
                        .padding(.horizontal, 40)  // 📏 Горизонтальные отступы: 40 пунктов
                        
                        // 🔄 ============================================
                        // 🔀 ПЕРЕКЛЮЧАТЕЛЬ РЕЖИМОВ
                        // 🔄 ============================================
                        Button(action: {  // 🔘 Кнопка для переключения между входом и регистрацией
                            
                            // 📝 ЛОГИРОВАНИЕ: переключение режима
                            print("🔄 [LoginView] Переключение режима: было '\(viewModel.isSignUpMode ? "РЕГИСТРАЦИЯ" : "ВХОД")'")
                            
                            withAnimation {  // 🎬 Применение анимации к изменениям состояния
                                viewModel.isSignUpMode.toggle()  // 🔄 Переключение режима (true ↔ false)
                                viewModel.errorMessage = nil  // 🧹 Очистка сообщения об ошибке при смене режима
                            }
                            
                            // 📝 ЛОГИРОВАНИЕ: после переключения
                            print("🔄 [LoginView] Теперь режим: '\(viewModel.isSignUpMode ? "РЕГИСТРАЦИЯ" : "ВХОД")'")
                            
                        }) {
                            Text(viewModel.isSignUpMode ?  // 📄 Динамический текст подсказки
                                 "Уже есть аккаунт? Войти" :  // 💬 Если режим регистрации — показать текст для входа
                                 "Нет аккаунта? Зарегистрироваться")  // 💬 Если режим входа — показать текст для регистрации
                            .font(.footnote)  // 🔤 Размер шрифта: сноска (мелкий текст)
                            .foregroundColor(.blue)  // 💙 Цвет текста: синий (стиль ссылки)
                        }
                        
                        Spacer(minLength: 50)  // 📐 Пустое пространство минимальной высотой 50 пунктов (для отступа снизу)
                    }
                }
            }
            .navigationTitle("")  // 🏷️ Установка заголовка навигации: пустая строка (скрываем стандартный заголовок)
            .navigationBarHidden(true)  // 👁️ Скрытие навигационной панели (полностью убираем bar)
            .alert("Ошибка", isPresented: .constant(viewModel.errorMessage != nil)) {  // 🚨 Alert для отображения ошибок
                Button("OK") {  // 🔘 Кнопка закрытия alert
                    viewModel.errorMessage = nil  // 🧹 Очистка ошибки при нажатии OK
                }
            } message: {  // 💬 Блок сообщения alert
                Text(viewModel.errorMessage ?? "")  // 📄 Отображение текста ошибки (или пустой строки если nil)
            }
        }
        .fullScreenCover(isPresented: $viewModel.isLoggedIn) {  // 🖼️ Полноэкранное покрытие: показывается когда isLoggedIn = true
            HomeView(viewModel: viewModel)  // 🏠 Отображение HomeView с передачей ViewModel (экран после успешного входа)
        }
    }
}
