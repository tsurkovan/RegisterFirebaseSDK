// 📦 1. Импортируем фреймворк SwiftUI для создания интерфейса
import SwiftUI

// 🏗️ 2. Объявляем структуру CustomButton, которая соответствует протоколу View
struct CustomButton: View {
    
    // 🏷️ 3. Переменная только для чтения: текст кнопки (например, "Войти")
    let title: String
    
    // ⚡ 4. Замыкание (closure): действие, которое выполнится при нажатии
    let action: () -> Void
    
    // ⏳ 5. Флаг загрузки: по умолчанию false (кнопка в обычном состоянии)
    var isLoading: Bool = false
    
    // ✅ 6. Флаг доступности: по умолчанию true (кнопка активна)
    var isEnabled: Bool = true
    
    // 🎨 7. Тело представления: описывает, как кнопка выглядит на экране
    var body: some View {
        
        // 🖱️ 8. Кнопка SwiftUI: принимает действие и содержимое
        Button(action: action) {
            
            // ↔️ 9. Горизонтальный стек: размещает элементы в ряд (иконка + текст)
            HStack {
                
                // ⏳ 10. УСЛОВИЕ: Если идёт загрузка...
                if isLoading {
                    
                    // 🔄 11. Индикатор прогресса: крутящийся спиннер
                    ProgressView()
                        
                        // 🎨 12. Стиль спиннера: круглый, цвет белый
                        .progressViewStyle(CircularProgressViewStyle(tint: .white))
                    
                } else {
                    // 📝 13. ИНАЧЕ: Если загрузка завершена...
                    
                    // 🏷️ 14. Текст кнопки: отображает заголовок (title)
                    Text(title)
                        
                        // 🔤 15. Шрифт: жирный заголовок (headline)
                        .font(.headline)
                }
            }
            
            // 📐 16. Ширина: растягивает кнопку на всю доступную ширину
            .frame(maxWidth: .infinity)
            
            // 📏 17. Внутренний отступ: увеличивает размер кнопки на 16pt
            .padding(16)
            
            // 🎨 18. Фон: синий если активна, серый если неактивна
            .background(isEnabled ? Color.blue : Color.gray)
            
            // 🏷️ 19. Цвет текста: всегда белый
            .foregroundColor(.white)
            
            // 🔲 20. Скругление: делает углы кнопки закруглёнными (12pt)
            .cornerRadius(12)
            
            // 👁️ 21. Прозрачность: 100% если активна, 60% если неактивна
            .opacity(isEnabled ? 1 : 0.6)
        }
        
        // 🚫 22. Блокировка: отключает кнопку если неактивна ИЛИ идёт загрузка
        .disabled(!isEnabled || isLoading)
    }
}

